###question1
'''23. What is git stash?
Often, when you’ve been working on part of your project, things are in a messy state and you want to switch 
branches for some time to work on something else. The problem is, you don’t want to do a commit of half-done 
work just so you can get back to this point later. The answer to this issue is ""Git stash"".

Stashing takes your working directory that is, your modified tracked files and staged changes 
and saves it on a stack of unfinished changes that you can reapply at any time.'''
##question2
'''24. What is the function of ‘git stash apply’?
If you want to continue working where you had left your work then ‘git stash apply‘ command is used to bring back 
the saved changes onto your current working directory.'''